    private void btnGuardarEnvioActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnGuardarEnvioActionPerformed
    {//GEN-HEADEREND:event_btnGuardarEnvioActionPerformed
        if (!rSTFFechaEnv.getText().isEmpty())
        {
            Paqs obj = new Paqs();
            obj.setNum_guia(Integer.parseInt(rSTFNumeroGuia.getText()));
            obj.setFchEnt(rSTFFechaEnv.getText());
            MetodosBD.modificacion(obj, 2);
            tabContenidoRec.listarPaquetes(tabContenidoRec.tblPaquetesRec);
            tabContenidoEnv.listarPaquetes(tabContenidoEnv.tblPaquetesEnv);
            //Sonido de exito
            new Alertas().incorrect(5);
            dispose();
        }else
        {
            //Sonido de alerta
            new Alertas().incorrect(6);
            jLabel1.setForeground(Color.red);
            jLabel1.setText("Fecha invalida");
        }

    }//GEN-LAST:event_btnGuardarEnvioActionPerformed

